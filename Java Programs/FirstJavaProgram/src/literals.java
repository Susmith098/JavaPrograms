public class literals {
    public static void main(String[] args) {
        //literals in java
        byte age = 22;
        int age2 = 56;
        short age3 = 3;
        long dino = 433000213123123313L;
        char ch = 'A';
        float f1 = 6.5F;
        double d1 = 6.6D;
        boolean a = true;
        System.out.println(age);
        String str = "It's me!";
        System.out.println(str);
    }
}
